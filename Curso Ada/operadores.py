#   OPERAÇÕES MATEMÁTICAS (ARITMÉTICAS)

"""

Soma: +
Subtração: -
Multiplicação: *
Divisão: /
Divisão inteira: //
Resto da divisão: %
Potência: **

"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(20 // 3)
print(20 % 3)
print(2 ** 3)


#   OPERADORES BOOLEANOS (LÓGICOS / COMPARAÇÃO)

idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(idade1 > idade2) # > : maior que
print(idade1 <= idade2) # <= : menor ou igual 
print('Python' == 'python') # == : igual
print('banana' != 'abacaxi') # != : diferente
print(altura1 >= altura2) # >= : maior ou igual
print(altura2 > altura3) # > maior que