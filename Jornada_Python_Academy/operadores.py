# OPERADOR IS

x = True
y = True

print(x is y)
print('-'*50)

# OPERADOR AND

print(False and False) # False
print(False and True) # False
print(True and False) # False
print(True and True) # True
print('-'*50)

# OPERADOR OR

print(False or False) # False
print(False or True) # True
print(True or False) # True
print(True or True) # True
print('-'*50)

# OPERADOR NOT

print(not True) # False
print(not False) # True
print('-'*50)


# OPERADORES DE COMPARAÇÃO

# == : IGUAL
# > : MAIOR QUE
# < : MENOR QUE
# <= : MENOR OU IGUAL QUE
# >= : MAIOR OU IGUAL QUE
# != : DIFERENTE DE

x = 10
y = 20

print(x == y) # False
print(x > y) # False
print(x < y) # True
print(x >= y) # False
print(x <= y) # True
print(x != y) # True